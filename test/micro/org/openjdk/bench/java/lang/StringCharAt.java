package org.openjdk.bench.java.lang;

import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Measurement;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.annotations.OutputTimeUnit;
import org.openjdk.jmh.annotations.Scope;
import org.openjdk.jmh.annotations.Setup;
import org.openjdk.jmh.annotations.State;
import org.openjdk.jmh.annotations.Warmup;
import org.openjdk.jmh.infra.Blackhole;

import java.util.concurrent.TimeUnit;

@Fork(5)
@State(Scope.Thread)
@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.NANOSECONDS)
@Warmup(iterations = 5, time = 1, timeUnit = TimeUnit.SECONDS)
@Measurement(iterations = 5, time = 1, timeUnit = TimeUnit.SECONDS)
public class StringCharAt {

    private String latinString;
    private String cyrillicString;

    @Setup
    public void setup() {
        latinString = "Idealism is what precedes experience; cynicism is what follows.";
        StringBuilder sb = new StringBuilder(latinString.length());
        latinString.codePoints().forEach(cp -> sb.append(cp - 'a' + '\u0430')); // make random Russian characters
        cyrillicString = sb.toString();
    }

    @Benchmark
    public void charAt_latin1(Blackhole bh) {
        for (int i = 0; i < latinString.length(); i++) {
            bh.consume(latinString.charAt(i));
        }
    }

    @Benchmark
    public void charAt_UTF16(Blackhole bh) {
        for (int i = 0; i < cyrillicString.length(); i++) {
            bh.consume(cyrillicString.charAt(i));
        }
    }

}
